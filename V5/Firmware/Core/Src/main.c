typedef struct {
	uint8_t red;
	uint8_t green;
	uint8_t blue;
} color_t;

color_t Red = {100, 0 , 0};
color_t Orange = {80, 100, 0};
color_t Blue = {0, 0, 100};
color_t Green = {0, 255, 0};
color_t Purple = {20, 0, 80};
color_t White = {100, 100, 100};
color_t Black = {0, 0, 0};

bool RGB_State = false;

bool Compare_RGB_Color(color_t color1, color_t color2)
{
	return (
		color1.red == color2.red &&
		color1.green == color2.green &&
		color1.blue == color2.blue
	);
}

void Set_RGB_Button(color_t color)
{
	__HAL_TIM_SET_COMPARE(&htim4, TIM_CHANNEL_1, color.blue);
	__HAL_TIM_SET_COMPARE(&htim4, TIM_CHANNEL_2, color.green);
	__HAL_TIM_SET_COMPARE(&htim4, TIM_CHANNEL_3, color.red);

	RGB_State = !Compare_RGB_Color(color, Black);
}

void Blink_RGB_Button(color_t color)
{
	if (!RGB_State)
	{
		Set_RGB_Button(Red);
	}
	else
	{
		Set_RGB_Button(Black);
	}
}

void Init_RGB_Button()
{
	HAL_TIM_PWM_Start(&htim4, TIM_CHANNEL_1);
	HAL_TIM_PWM_Start(&htim4, TIM_CHANNEL_2);
	HAL_TIM_PWM_Start(&htim4, TIM_CHANNEL_3);

	Set_RGB_Button(Orange);
}

